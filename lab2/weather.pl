% ---------------------------------------------------
% ФАКТЫ: weather(Город, Температура_С, Состояние, Ветер_м_с)
% ---------------------------------------------------
% Состояние: солнечно, облачно, дождь, снег.

weather('Москва', 5, дождь, 7).
weather('Санкт-Петербург', 2, снег, 12).
weather('Казань', -3, снег, 4).
weather('Екатеринбург', -15, снег, 9).

% ---------------------------------------------------
% ПРАВИЛА: рекомендации по температуре
% ---------------------------------------------------

temp_advice(Temp, 'Очень холодно, одевайтесь максимально тепло!') :-
    Temp < -10.

temp_advice(Temp, 'Холодно, наденьте теплую куртку и шапку.') :-
    Temp >= -10,
    Temp < 0.

temp_advice(Temp, 'Прохладно, возьмите ветровку или легкую куртку.') :-
    Temp >= 0,
    Temp < 10.

temp_advice(Temp, 'Тепло, можно гулять в джемпере или рубашке.') :-
    Temp >= 10,
    Temp < 20.

temp_advice(Temp, 'Жарко, лучше надеть футболку и шорты.') :-
    Temp >= 20.

% ---------------------------------------------------
% ПРАВИЛА: рекомендации по состоянию погоды
% ---------------------------------------------------

cond_advice(дождь, 'Возьмите зонт, ожидается дождь.').
cond_advice(снег, 'Осторожно, снегопад, будьте аккуратны на дорогах.').
cond_advice(солнечно, 'Не забудьте солнечные очки и головной убор!').
cond_advice(облачно, 'Погода облачная, осадков не ожидается.').

% ---------------------------------------------------
% ПРАВИЛА: рекомендации по ветру
% ---------------------------------------------------

wind_advice(Wind, 'Сильный ветер, будьте осторожны!') :-
    Wind > 10.

wind_advice(Wind, 'Ветрено, застегните куртку.') :-
    Wind >= 5,
    Wind =< 10.

wind_advice(Wind, 'Ветер слабый, комфортно.') :-
    Wind < 5.

% ---------------------------------------------------
% ПРАВИЛА: рекомендации активности
% ---------------------------------------------------

activity_advice(Temp, солнечно, 'Идеально для пляжа!') :-
    Temp > 20.

activity_advice(Temp, дождь, 'Можно посетить музей или кафе.') :-
    Temp >= 15,
    Temp =< 25.

activity_advice(Temp, снег, 'Хорошая погода для катания на лыжах.') :-
    Temp < 0.

activity_advice(_, _, 'Можно просто погулять по городу.').

% ---------------------------------------------------
% ОБЩЕЕ ПРАВИЛО: полный совет по городу
% advice_for(Город, ПолныйСовет)
% ---------------------------------------------------

advice_for(City, FullAdvice) :-
    weather(City, Temp, Cond, Wind),
    temp_advice(Temp, TempPart),
    cond_advice(Cond, CondPart),
    wind_advice(Wind, WindPart),
    activity_advice(Temp, Cond, ActivityPart),
    atomic_list_concat(
        [TempPart, ' ', CondPart, ' ', WindPart, ' ', ActivityPart],
        FullAdvice
    ).

% ---------------------------------------------------
% ВЫВОД ВСЕХ СОВЕТОВ ДЛЯ ГОРОДОВ
% ---------------------------------------------------

show_all_advices :-
    forall(
        weather(City, _, _, _),
        (
            advice_for(City, Advice),
            format('~w: ~w~n', [City, Advice])
        )
    ).

